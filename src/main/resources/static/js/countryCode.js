function optSelValue(){
    //선택한 언어 value 가져오기
    var language = document.querySelector("#language");
    var optSel = language.value;

    //선택한 언어 번역하여 변수 선언
    var changeList = rangList[optSel]; //대한민국
    var changeRememberMe = rangRememberMe[optSel]; //RememberMe
    var changeForgotPassword = rangForgotPassword[optSel]; //비밀번호를 잊어 버렸습니까?
    var changeSignUp = rangSignUp[optSel]; //가압하기
    var changeCheckID = rangCheckID[optSel]; //중복 ID 확인
    var changeChangePassword = rangChangePassword[optSel]; //비밀번호 변경하기
    var changeNewPassword = rangNewPassword[optSel]; //새 비밀번호
    var changeNewPasswordCheck = rangNewPasswordCheck[optSel]; //새 비밀번호 확인
    var changeDelAccount = rangDelAccount[optSel]; //계정삭제
    var changeDelAccountCheck = rangDelAccountCheck[optSel]; //탈퇴하시겠습니까?
    var changeAccount = rangAccount[optSel]; //계정
    var changeProgress = rangProgress[optSel]; //진행상황
    var changeDistribution = rangDistribution[optSel]; //정규분포
    var changeMake = rangMake[optSel]; //만들기

    //url 선택
    var urlHtml = document.querySelector(".url").id;
    switch(urlHtml){
        case "url-index":
            document.querySelector(".remember").querySelector("label").innerText = changeRememberMe; //"아이디중복확인"
            document.querySelector(".lostpass").querySelector("a").innerText = changeForgotPassword; //"아이디중복확인"
            break;

        case "url-billySignup":
            document.querySelector(".id_overlep").querySelector("input").value = changeCheckID; //"아이디중복확인"
            document.querySelector(".submit").querySelector("input").value = changeSignUp; //"가입하기"
            break;

        case "url-billyAbility":
            document.querySelector(".cl-changeAccount").querySelector("a").innerText = changeAccount; //"계정"
            document.querySelector(".graph_name").innerText = changeProgress; //"진행상황"
            document.querySelector(".graph_name").innerText = changeDistribution; //"정규분포"
            document.querySelector(".tags_contain").querySelector("form").querySelector("input[type=submit]").value = changeMake; //"만들기"
            break;


        case "url-billyPersonal":
                document.querySelector(".change-pw").innerHTML = changeChangePassword; //"비밀번호 변경하기"
                document.querySelector(".change-pw-new").placeholder = changeNewPassword; //"새 비밀번호"
                document.querySelector(".change-pw-new-cha").placeholder = changeNewPasswordCheck; //"새 비밀번호 확인"
                document.querySelector(".change-submit-pw").value = changeChangePassword; //"비밀번호 변경하기"
                document.querySelector(".del_account").querySelector("p").innerHTML = changeDelAccount; //"계정삭제"
                document.querySelector(".del_account").querySelector("div").querySelector("input").value = changeDelAccountCheck; //"탈퇴하시겠습니까?"
            break;

        case "url-billyLostPassword":
                break;
        

        case "url-billyWordsTest":
            break;
        
        default:
            break;
    }
}

